#include <unistd.h>

void	rev_print(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
		x++;
	while (x > 0)
	{
		write(1, &str[x - 1], 1);
		x--;
	}
}

int	main(int ac, char *av[])
{
	if (ac == 2)
	{
		rev_print(av[1]);
	}
	write(1, "\n", 1);
}