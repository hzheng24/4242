#include <stdio.h>
#include <string.h>

char	*ft_strcat(char *dest, char *src)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (dest[x] != '\0')
	{
		x++;
	}
	while (src[y] != '\0')
	{
		dest[x] = src[y];
		x++;
		y++;
	}
	dest[x] = '\0';
	return (dest);
}

int	main(void)
{
	char x[50] = "Hello";
	char y[] = "World";

	ft_strcat(x, y);
	printf("%s\n", x, y);
	return (0);
}