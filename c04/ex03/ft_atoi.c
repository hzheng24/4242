int	ft_atoi(char *str)
{
	int x;
	int signo;

	x = 0;
	signo = 1;
	while ((*str == ' ') || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			signo *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		x *= 10;
		x += (signo * (*str - 48));
		str++;
	}
	return (x);
}
