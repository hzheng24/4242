void	ft_rev_int_tab(int *tab, int size)
{
	int c;
	int inv;
	int p;

	inv = size - 1;
	p = 0;
	while (inv >= p)
	{
		c = tab[inv];
		tab[inv] = tab[p];
		tab[p] = c;
		p++;
		inv--;
	}
}
