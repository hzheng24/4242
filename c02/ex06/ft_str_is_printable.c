int	ft_str_is_printable(char *str)
{
	int x;

	if (str == '\0')
		return (0);
	x = 0;
	while (str[x] != '\0')
	{
		if (!(str[x] >= 32 && str[x] < 127))
		{
			return (0);
		}
		x++;
	}
	return (1);
}
